// Створення масиву чисел
const numbers = [10, 20, 30, 40, 50];

// Ініціалізація змінної для зберігання суми
let sum = 0;

// Обчислення суми елементів масиву за допомогою циклу For
for (let i = 0; i < numbers.length; i++) {
    sum += numbers[i];
}

// Виведення результату на консоль
console.log("Сума елементів масиву:", sum);
